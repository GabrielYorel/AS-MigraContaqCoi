    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try {
            CrearConexionFBoSQL objConexion = new CrearConexionFBoSQL(controlador, servidor, puerto,
                    RutaonombreBDCont, usuario, contrasenia,seguridadIntegrada);
            conexion = objConexion.getConexion();

            if (conexion != null) {
                if (controlador.equals("DevartSQLServer")) {
                    Statement stmt = conexion.createStatement();
                    ResultSet rs = stmt.executeQuery("Select Id,Ejercicio from Ejercicios order by Id");
                    while (rs.next()) {
                        cboEjercicios.addItem(new EjercicioEntity(rs.getInt(1), rs.getString(2)));
                    }
                    rs.close();
                    conexion.close();
                }
                if (cboEjercicios.getItemCount() > 0) {
                    rbtnPrimerEjercicio.setEnabled(true);
                    rbtnSeleccionado.setEnabled(true);
                } else {
                    rbtnPrimerEjercicio.setEnabled(false);
                    rbtnSeleccionado.setEnabled(false);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_formWindowOpened

